// Handle download record button click
$(document).on('click', '.download-record', function () {
  var filePath = $(this).data('file');

  // Perform file download
  window.location.href = '/download?url=' + filePath;
});

  // Delete Record
  $(document).on('click', '.delete-record', function () {
    var user_id = $(this).data('id'),
      dtrModal = $('.dtr-bs-modal.show');

    // hide responsive modal in small screen
    if (dtrModal.length) {
      dtrModal.modal('hide');
    }

    // sweetalert for confirmation of delete
    Swal.fire({
      title: 'Are you sure?',
      text: "You won't be able to revert this!",
      icon: 'warning',
      showCancelButton: true,
      confirmButtonText: 'Yes, delete it!',
      customClass: {
        confirmButton: 'btn btn-primary me-3',
        cancelButton: 'btn btn-label-secondary'
      },
      buttonsStyling: false
    }).then(function (result) {
      if (result.value) {
        // delete the data
        $.ajax({
          type: 'DELETE',
          url: `${baseUrl}MedicalFile-list/${user_id}`,
          success: function () {
            dt_user.draw();
          },
          error: function (error) {
            console.log(error);
          }
        });

        // success sweetalert
        Swal.fire({
          icon: 'success',
          title: 'Deleted!',
          text: 'The Medical file has been deleted!',
          customClass: {
            confirmButton: 'btn btn-success'
          }
        });
      } else if (result.dismiss === Swal.DismissReason.cancel) {
        Swal.fire({
          title: 'Cancelled',
          text: 'The Medical file is not deleted!',
          icon: 'error',
          customClass: {
            confirmButton: 'btn btn-success'
          }
        });
      }
    });
  });
  //show record
  $(document).on('click', '.view-record', function(event) {
    event.preventDefault(); // Prevent the default link behavior

    var patientId = $(this).data('id');

    // Make an AJAX request to the show route
    $.ajax({
        url: '/MedicalFile-list/' + patientId, // Adjust the URL according to your route setup
        method: 'GET',
        success: function(response) {
            // Handle the successful response, for example, redirecting to the show page
            window.location.href = '/MedicalFile-list/' + patientId;
        },
        error: function(xhr, status, error) {
            // Handle any errors, for example, displaying an error message
            console.error(xhr.responseText);
        }
    });
});

  // edit record
  $(document).on('click', '.edit-record', function () {
    var user_id = $(this).data('id'),
      dtrModal = $('.dtr-bs-modal.show');

    // hide responsive modal in small screen
    if (dtrModal.length) {
      dtrModal.modal('hide');
    }

    // changing the title of offcanvas
    $('#offcanvasAddMedicalFileLabel').html('Edit File');

    // get data
    $.get(`${baseUrl}MedicalFile-list\/${user_id}\/edit`, function (data) {

      $('#medical_file_id').val(data.id);
      $('#add-medical-file-name').val(data.file_name);
      $('#add-medical-file-description').val(data.description);
    });
  });

  // changing the title
  $('.add-new').on('click', function () {
    $('#user_id').val(''); //reseting input field
    $('#offcanvasAddMedicalFileLabel').html('Add File');

  });

  // Filter form control to default size
